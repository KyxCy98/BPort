void typewriter(const char* str, int delay_microseconds) {
    int len = strlen(str);
    for (int i = 0; i < len; i++) {
        putchar(str[i]);
        fflush(stdout);
        usleep(delay_microseconds);
    }
    putchar('\n');
}

